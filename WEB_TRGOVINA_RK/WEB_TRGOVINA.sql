--Potrebna nam je detaljna analiza svih digitalnih raèuna koje smo izdali poslije 2018. godine.
--Trebamo prikazati prezime i ime (u formatu "prezime, ime") svakog izdavatelja raèuna i korisnika poslije te godine.
--Uz korisnika je potreban i njihov "OIB", odnosno jedinstven numerièki identifikator. Takoðer nam je bitno da toèno saznamo kontinent i državu u kojoj se korisnik nalazi,
--te poštanski broj na koji su poslani ti proizvodi, ako je korisnik na raèunu uopæe dao poštanski broj. Ako nije to trebamo u prikazu istaknuti.
--Potrebna nam je i informacija o tome koliko je kupac potrošio na pojedinu stavku raèuna, koliko su pojedinog proizvoda kupili,
--u koju kategoriju spadaju ti proizvodi, te iz kojeg su skladišta došli. Takoðer, potreban nam je i datyum same kupnje.
--Bitno je i na koji je naèin izvedena sama kupnja (pouzeæem ili karticom), potrebno je ispisati broj kartice ako ju je korisnik dao.
--Prikaz sortirati po datumu kupnje, pa po prezimenima kupaca.
SELECT 
Z.PREZIME || ', ' || Z.IME AS "PREZIME, IME IZDAVAÈA",
K.PREZIME || ', ' || K.IME AS "PREZIME, IME KUPCA",
K.OIB AS "OIB KUPCA",
KON.NAZIV_KONTINENTA AS "KONTINENT KUPCA",
D."NAZIV_DRŽAVE" AS "DRŽAVA KUPCA",
COALESCE(G."POŠTANSKI_BROJ", 'Nema podatka') AS "POŠTANSKI BROJ KUPCA",
S."ADRESA_SKLADIŠTA" AS "ADRESA SKLADIŠTA",
KAT.NAZIV_KATEGORIJE AS "KATEGORIJA PROIZVODA",
P.NAZIV_PROIZVODA AS "STAVKA RAÈUNA",
RP.KOLIÈINA AS "KOLIÈINA KUPLJENA",
R.DATUM_KUPNJE AS "DATUM KUPNJE",
CASE
  WHEN R."NAÈIN_PLAÆANJA" = 'K' THEN 'Karticom'
  ELSE 'Pouzeæem'
END AS "NAÈIN PLAÆANJA",
COALESCE(K.KARTICA, 'Nema podatka') AS "KARTICA KUPCA"
FROM "RAÈUNI" R
INNER JOIN "RAÈUNI_PROIZVODI" RP ON RP."RAÈUNI_ID_RAÈUNI" = R."ID_RAÈUNI"
INNER JOIN ZAPOSLENICI Z ON Z.ID_ZAPOSLENIK = R.ZAPOSLENICI_ID_ZAPOSLENIK
INNER JOIN KORISNICI K ON K.ID_KORISNIK = R.KORISNICI_ID_KORISNIK
INNER JOIN GRADOVI G ON G.ID_GRAD = K.GRADOVI_ID_GRAD
INNER JOIN "DRŽAVE" D ON D."ID_DRŽAVE" = G."DRŽAVE_ID_DRŽAVE"
INNER JOIN "DRŽAVE_KONTINENTI" DK ON DK."DRŽAVE_ID_DRŽAVE" = D."ID_DRŽAVE"
INNER JOIN KONTINENTI KON ON KON.ID_KONTINENTI = DK.KONTINENTI_ID_KONTINENTI
INNER JOIN PROIZVODI P ON P.ID_PROIZVOD = RP.PROIZVODI_ID_PROIZVOD
INNER JOIN "PROIZVODI_SKLADIŠTA" PS ON PS.PROIZVODI_ID_PROIZVOD = P.ID_PROIZVOD
INNER JOIN "SKLADIŠTA" S ON S."ID_SKLADIŠTE" = PS."SKLADIŠTA_ID_SKLADIŠTE"
INNER JOIN KATEGORIJE KAT ON KAT.ID_KATEGORIJE = P.KATEGORIJE_ID_KATEGORIJE
WHERE EXTRACT(YEAR FROM R.DATUM_KUPNJE) > 2018
ORDER BY 11, 2;