--Znaèi, potreban nam je ispis (prikazanog kao entitet) na kojem æemo saznati, kao prvo,
--o kojim se zaposlenicima radi, ako još uvijek rade(tj. traže se osnovni osobni podaci, tj. ime, prezime, OIB, spol, te datum i mjesto roðenja) ili upisati "Obrisano" ako više nemaju te podatke sadržane u bazi podataka,
--treba nam i njihova trenutna, odnosno bivša funkcija u radu za web trgovinu, te od kad, (pa i eventualno do kad) su izvršavali tu funkciju, s tim da ako im istek ugovora nije odreðen onda to trebamo i naznaèiti
--, takoðer odmah do tog podatka je željen taj isti podatak, no prikazan u formatu broja dana rada za web trgovinu radi lakšeg išèitavanja.
--Sve ove podatke, radi boljeg pregleda, sortirati silazno po broju dana rada za web trgovinu.
SELECT 
COALESCE(NULLIF(Z.IME || ' ' || Z.PREZIME, ' '), 'Obrisano') AS "IME I PREZIME",
COALESCE(Z.OIB, 'Obrisano') AS OIB,
COALESCE(S.NAZIV_SPOLA, 'Obrisano') AS "SPOL",
COALESCE(TO_CHAR(Z."DATUM_ROÐENJA"), 'Obrisano') AS "DATUM ROÐENJA",
COALESCE(G.NAZIV_GRADA, 'Obrisano') AS "MJESTO ROÐENJA",
F.NAZIV_FUNKCIJE AS "FUNKCIJA",
F.OD_KAD as "POÈETAK UGOVORA",
COALESCE(TO_CHAR(F.DO_KAD), 'Neodreðeno') AS "KRAJ UGOVORA",
COALESCE(F.DO_KAD - F.OD_KAD, TRUNC(SYSDATE) - F.OD_KAD) || ' ' || 'dan(a)' AS "BROJ DANA RADA"
FROM ZAPOSLENICI Z
RIGHT JOIN FUNKCIJE_ZAPOSLENIKA F ON F.ID_FUNKCIJA_ZAPOSLENIKA = Z.FUNKCIJE_ZAPOSLENIKA_ID2
LEFT JOIN SPOLOVI S ON S.ID_SPOL = Z.SPOLOVI_ID_SPOL
LEFT JOIN GRADOVI G ON G.ID_GRAD = Z.GRADOVI_ID_GRAD
ORDER BY COALESCE(F.DO_KAD - F.OD_KAD, TRUNC(SYSDATE) - F.OD_KAD) DESC;

