--1. SELECT UPIT
--Menadžer web trgovine traži izvještaj o pregledu inventara. 
--Traže se podaci o ukupnoj vrijednosti (u eurima) i kolièini proizvoda u inventaru svakog pojedinog skladišta,
--te gdje se uopæe nalazi to skladište (grad i država).
--Izvještaj, to jest tablica mora biti poredana, odnosno sortirana, silazno, po vrijednosti unutar skladišta.
SELECT 
D."NAZIV_DRŽAVE" AS "NAZIV DRŽAVE",
G."NAZIV_GRADA" AS "NAZIV GRADA",
S."ADRESA_SKLADIŠTA" AS "ADRESA SKLADIŠTA",
SUM(PS."KOLIÈINA") AS "UKUPNA KOLIÈINA",
SUM(P.CIJENA * PS."KOLIÈINA") || ' ' || 'eura' AS "UKUPNA VRIJEDNOST"
FROM "SKLADIŠTA" S
INNER JOIN GRADOVI G ON G.ID_GRAD = S.GRADOVI_ID_GRAD
INNER JOIN "DRŽAVE" D ON D."ID_DRŽAVE" = G."DRŽAVE_ID_DRŽAVE"
INNER JOIN "PROIZVODI_SKLADIŠTA" PS ON PS."SKLADIŠTA_ID_SKLADIŠTE" = S."ID_SKLADIŠTE"
INNER JOIN PROIZVODI P ON P.ID_PROIZVOD = PS."PROIZVODI_ID_PROIZVOD"
GROUP BY D."NAZIV_DRŽAVE", G."NAZIV_GRADA", S."ADRESA_SKLADIŠTA"
ORDER BY 5 DESC;

--2. SELECT UPIT
--Web trgovina pokušava održati nagradnu igru koja se temelji na principu lutrije.
--Izvlaèe se kuglice sa razlièitim obilježjima, te oni korisnici koji zadovoljavaju sva ta obilježja dobivaju (još neodreðenu) nagradu.
--Nakon izvlaèenja kuglica, dobivena obilježja korisnika moraju biti sljedeæa: 
--korisnik ne smije biti oznaèen na profilu kao muškarac, i njihovo ime i njihovo prezime moraju sadržavati slovo "n", trebali su ukupno potrošiti više od 100, a manje od 600 eura na web trgovini,
--te njihov OIB (odnosno jedinstven identifikator) mora sadržavati broj "78" i još mora vrijediti da su roðeni u prošlom tisuæljeæu.
--Traži se prikaz svih korisnika koji zadovoljavaju ova obilježja, toènije:
--njihovo ime i prezime, jedinstveni "OIB" tih korisnika, datum roðenja, njihov spol, te koliko su ukupno potrošili novca na web trgovini.
SELECT
K.IME || ' ' || K.PREZIME AS "IME I PREZIME",
K.OIB,
K."DATUM_ROÐENJA" AS "DATUM ROÐENJA",
S.NAZIV_SPOLA AS "SPOL",
K."UKUPNO_PLAÆENO" AS "UKUPNO POTROŠENO"
FROM KORISNICI K
INNER JOIN SPOLOVI S ON S.ID_SPOL = K.SPOLOVI_ID_SPOL
WHERE K.SPOLOVI_ID_SPOL != 1
AND (K.IME LIKE '%N%' OR K.IME LIKE '%n%')
AND (K.PREZIME LIKE '%N%' OR K.PREZIME LIKE '%n%')
AND K."UKUPNO_PLAÆENO" IN (
  SELECT DISTINCT K."UKUPNO_PLAÆENO"
  FROM KORISNICI K
  WHERE K."UKUPNO_PLAÆENO" > 100 AND K."UKUPNO_PLAÆENO" < 600
)AND K.OIB LIKE '%78%'
AND EXTRACT(YEAR FROM K."DATUM_ROÐENJA") <= 2000;

